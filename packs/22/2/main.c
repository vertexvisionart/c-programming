#include "modular.h" // в этом хедере должны быть объявления
#include <assert.h>

int main() {
    // Базовые проверки из условия задачи
    MOD = 13; // устанавливаем глобальный модуль
    int a = 45;
    a = pnorm(a);
    assert(a == 6);
    
    int x = pmul(padd(7, psub(2, 3)), 5);
    assert(x == 4);
    
    int y = pdiv(7, x);
    assert(pmul(x, y) == 7);
    
    MOD = 2; // меняем модуль на другой
    assert(pnorm(5) == 1);
    
    // ДОПОЛНИТЕЛЬНЫЕ ТЕСТЫ ДЛЯ ПРОВЕРКИ КРАЙНИХ СЛУЧАЕВ
    
    MOD = 1000000007; // Большой простой модуль (около 10^9)
    
    // Тест pnorm с отрицательными числами
    assert(pnorm(-1) == 1000000006);
    assert(pnorm(-1000000008) == 1000000006);
    
    // Тест padd с переходом через модуль
    assert(padd(1000000006, 5) == 4);
    
    // Тест psub с получением отрицательного результата
    assert(psub(2, 5) == 1000000004);
    
    // Тест pmul (проверка переполнения типа int)
    // 1000000006 * 2 = 2000000012 
    // 2000000012 % 1000000007 = 1000000005
    assert(pmul(1000000006, 2) == 1000000005);
    
    // Тесты pdiv
    assert(pdiv(100, 25) == 4);            // Обычное деление нацело
    assert(pdiv(1, 2) == 500000004);       // Обратный элемент: (1/2) mod (10^9+7) = 500000004
    assert(pmul(500000004, 2) == 1);       // Проверка обратного элемента

    return 0;
}